-- Create a database schema for a simple e-commerce system, including 
-- tables for products, customers, orders, and order items.

create database schemaa;
create table sche_products (pro_id int, pro_name varchar(10));
create table sche_customers(cust_id int, cust_name varchar(10));
create table sche_orders (order_id int, order_name varchar(10));
create table sche_order_items ( items_id int, item_name varchar(10));